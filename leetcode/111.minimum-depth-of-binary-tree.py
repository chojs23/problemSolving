# @lc code=start
# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right

from collections import deque


class Solution:
    def minDepth(self, root):
        q = deque()
        if root:
            q.append(root)
        ans = 0
        while q:
            ans += 1
            for i in range(len(q)):
                node = q.popleft()
                if node.left:
                    q.append(node.left)
                if node.right:
                    q.append(node.right)
                if not (node.left or node.right):
                    return ans

        return ans


